#include <iostream>

using namespace std;

int main()
{
    int n;
    cin >> n;
    switch(n)
    {
        case 1: cout << "i" << endl;
        break;

        case 2: cout << "ii" << endl;
        break;

        case 3: cout << "iii" << endl;
        break;

        case 4: cout << "iv" << endl;
        break;

        case 5: cout << "v" << endl;
        break;

    }

    return 0;
}
